      Function IFEQU(arg1,arg2)
CC
CC---------------------------------------------------------------------
CC--- Author organization- H. E. Cramer Co., Inc.                   ---
CC--- Subroutine code - IFEQU Version 01.0                          ---
CC--- Subroutine description - used to avoid nasty warning messages ---
CC---                          generated by compilers that do not   ---
CC---                          like real variable comparisons using ---
CC---                          .eq. or .ne.                         ---
CC---------------------------------------------------------------------
CC
CC        returns - IFEQU
CC                  = 1, if arg1 > arg2
CC                  =-1, if arg1 < arg2
CC                  = 0, otherwise
CC
CC**********************************************************************
CC
      if (arg1 .gt. arg2) then
        IFEQU = 1
      else if (arg1 .lt. arg2) then
        IFEQU = -1
      else
        IFEQU = 0
      end if
      return
      end
